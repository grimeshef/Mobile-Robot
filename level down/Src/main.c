void fwd(float persent, double plavn)
{
    int znach = (th_mid + (th_max - th_mid) * persent);
    if (plavn == 0)
    {
        TIM4->CCR1 = znach;
    }
    else
    {
        while (TIM4->CCR1 < znach)
        {
            TIM4->CCR1 = TIM4->CCR1 + 1;
            HAL_Delay(plavn);
            waiting = waiting + plavn;
        }
    }
}

void rev(float persent, double plavn)
{
    int znach = (th_mid - (th_mid - th_min) * persent);
    if (plavn == 0)
    {
        TIM4->CCR1 = znach;
    }
    else
    {
        while (TIM4->CCR1 > znach)
        {
            TIM4->CCR1 = TIM4->CCR1 - 1;
            HAL_Delay(plavn);
            waiting = waiting + plavn;
        }
    }
}

